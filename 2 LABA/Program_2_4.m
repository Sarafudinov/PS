% Программа P2_4
% Генерация входных последовательностей
clf;
n=0:40; D=10; a=3.0; b=-2;
x=a*cos(2*pi*0.1*n)+b*cos(2*pi*0.4*n);
xd=[zeros(1,D) x];
num=[2.2403 2.4908 2.2403];
den=[1 -0.4 0.75];
ic=[0 0]; % Установка начальных условий
% Вычисление выходного сигнала y[n]
y=filter(num,den,x,ic);
% Вычисление выходного сигнала yd[n]
yd=filter(num,den,xd,ic);
% Вычисление сигнала разности d[n]
d=y-yd(1+D:41+D);
% Вывод графиков выходных сигналов
subplot(3,1,1);
stem(n,y);
ylabel('Амплитуда');
title('Выходной сигнал y[n]'); grid;
subplot(3,1,2);
stem(n,yd(1:41));
ylabel('Амплитуда');
title(['Выход, соответствующий отстающему входу x[n ',num2str(D),']']); grid;
subplot(3,1,3);
stem(n,d);
xlabel('Время n'); ylabel('Амплитуда');
title('Сигнал разности'); grid;
